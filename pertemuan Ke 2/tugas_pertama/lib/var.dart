// Contoh penerapan Variabel pada pemograman Dart
void main() {
  // Melakukan deklarasi variabel
  double panjang;
  double lebar;
  double luas;
  double keliling;

  panjang = 10;
  lebar = 7;

  luas = panjang * lebar;
  keliling = 2 * (panjang + lebar);

  print('Luas persegi panjang \t :' + luas.toString());
  print('Keliling persegi panjang \t : ' + keliling.toString());
}
